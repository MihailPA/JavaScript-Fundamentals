//  Problem 3. Rectangle area
//  Write an expression that calculates rectangle’s area by given width and height.

function rectangleArea(x, y) {
    return x * y;
}

console.log('x = 3, y = 4, Area=' + rectangleArea(3, 4));
console.log('x = 2.5, y = 3, Area=' + rectangleArea(2.5, 3));
console.log('x = 5, y = 5, Area=' + rectangleArea(5, 5));